#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*rtn;
	size_t	i;

	rtn = NULL;
	i = ft_strlen(s);
	while (s[i] >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	if (s[i] == (char)c)
		return ((char *)(s + i));
	return (NULL);
}
